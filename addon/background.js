


/***********************************************************************************************************************************
* Logger class
***********************************************************************************************************************************/
class Logger {
	
	
	/*******************************************************************************************************************************
	* Defined log levels
	*******************************************************************************************************************************/
	static LOGLVL_ERR = 1;
	static LOGLVL_WARN = 2;
	static LOGLVL_INFO = 3;
	static LOGLVL_DEBUG = 4;
	
	
	/*******************************************************************************************************************************
	* Logger constructor
	*******************************************************************************************************************************/
	constructor(loglevel) {
		this._level = loglevel;
		
		// Set default log level if none passed
		if (!this._level) this._level = Logger.LOGLVL_INFO;
	}
	
	
	/*******************************************************************************************************************************
	* Logger print function for all log levels
	*******************************************************************************************************************************/
	print(level, msg) {
		if (level > this._level) return;
		
		console.log(msg);
	}
	
	
	/*******************************************************************************************************************************
	* Error logger
	*******************************************************************************************************************************/
	error(msg) {
		this.print(Logger.LOGLVL_ERR, msg);
	}
	
	
	/*******************************************************************************************************************************
	* Warning logger
	*******************************************************************************************************************************/
	warn(msg) {
		this.print(Logger.LOGLVL_WARN, msg);
	}
	
	
	/*******************************************************************************************************************************
	* Informational logger
	*******************************************************************************************************************************/
	info(msg) {
		this.print(Logger.LOGLVL_INFO, msg);
	}
	
	
	/*******************************************************************************************************************************
	* Debug logger
	*******************************************************************************************************************************/
	debug(msg) {
		this.print(Logger.LOGLVL_DEBUG, msg);
	}
	
}



/***********************************************************************************************************************************
* Global logger instance
***********************************************************************************************************************************/
var logger = new Logger(Logger.LOGLVL_DEBUG);



/***********************************************************************************************************************************
* Main class
***********************************************************************************************************************************/
class Main {
	
	
	/*******************************************************************************************************************************
	* Main constructor
	*******************************************************************************************************************************/
	constructor() {
		this._comm = null;
	}
	
	
	/*******************************************************************************************************************************
	* Addon entry point
	*******************************************************************************************************************************/
	start() {
		this._comm = new Comm(new Handler());
		this._comm.connect();
	}
	
	/*******************************************************************************************************************************
	* Send an unsolicited message
	*******************************************************************************************************************************/
	unso(msg) {
		console.log('unso2');
		this._comm.send(msg);
	}
	
}



/***********************************************************************************************************************************
* Main instance
***********************************************************************************************************************************/
let main = new Main();



/***********************************************************************************************************************************
* Initial installation handler, for use when side-loading addon
***********************************************************************************************************************************/
browser.runtime.onInstalled.addListener(() => {
	logger.debug('Addon installed');
	main.start();
});



/***********************************************************************************************************************************
* Initial startup handler, for use when addon is loaded normally
***********************************************************************************************************************************/
browser.runtime.onStartup.addListener(() => {
	logger.debug('Addon started');
	main.start();
});


/***********************************************************************************************************************************
* Alarm-based reconnect — fires every minute when disconnected, bypassing Firefox timer throttling
***********************************************************************************************************************************/
browser.alarms.onAlarm.addListener((alarm) => {
	if (alarm.name === 'reconnect' && !main._comm._connected) {
		logger.info('Alarm: reconnecting to service');
		main._comm.connect();
	}
});



/***********************************************************************************************************************************
* Unsolicited event from content script
***********************************************************************************************************************************/
browser.runtime.onMessage.addListener((msg, sender) => {
	console.log('unso1');
	msg.tab = sender.tab.id;
	main.unso(msg);
});



